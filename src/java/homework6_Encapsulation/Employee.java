package homework6_Encapsulation;

public class Employee {
    String name;
    String position;
    String email;
    int phone_number;
    int age;

    public Employee(String name, String position, String email, int phone_number, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.phone_number = phone_number;
        this.age = age;
    }
}
